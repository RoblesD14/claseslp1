<? php

clase  Operaciones {
     función estática  pública suma ( $ nro1 , $ nro2 ): void { 
        $ resultado = $ nro1 + $ nro2 ;
        echo  "el resultado de la suma es:" . $ resultado ;
    }

     función pública estática  resta ( $ nro1 , $ nro2 ): void { 
        $ resultado = $ nro1 - $ nro2 ;
        echo  "el resultado de la resta es:" . $ resultado ;
    }

     multiplicación de la función estática  pública ( $ nro1 , $ nro2 ): void { 
        $ resultado = $ nro1 * $ nro2 ;
        echo  "el resultado de la multiplicación es:" . $ resultado ;
    }

     división de función estática  pública ( $ nro1 , $ nro2 ): void { 
        $ resultado = $ nro1 / $ nro2 ;
        echo  "el resultado de la división es:" . $ resultado ;
    }

}